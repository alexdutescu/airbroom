import ro.alex.client.scaffold.place.ProxyPlace;

public class ZborActivitiesMapper extends ZborActivitiesMapper_Roo_Gwt {

    public ZborActivitiesMapper(ApplicationRequestFactory requests, PlaceController placeController) {
        this.requests = requests;
        this.placeController = placeController;
    }

    public Activity getActivity(ProxyPlace place) {
        switch(place.getOperation()) {
            case DETAILS:
                return new ZborDetailsActivity((EntityProxyId<ZborProxy>) place.getProxyId(), requests, placeController, ScaffoldApp.isMobile() ? ZborMobileDetailsView.instance() : ZborDetailsView.instance());
            case EDIT:
                return makeEditActivity(place);
            case CREATE:
                return makeCreateActivity();
        }
        throw new IllegalArgumentException("Unknown operation " + place.getOperation());
    }
}
